SELECT U.NAME as name, IFNULL(SUM(DISTANCE),0) AS travelled_distance
FROM USERS AS U LEFT JOIN RIDES AS R ON U.ID = R.USER_ID
GROUP BY U.ID, U.NAME
ORDER BY travelled_distance DESC, NAME ASC

